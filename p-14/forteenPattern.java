public class forteenPattern {
    public static void main(String[] args){
        
        for(int i = 0; i < 5; i++){
            char alpha = 'A';
            for(int j = 0; j <= i; j++){
                System.out.print(" " + alpha + " ");
                alpha++;
            }
            System.out.println();
            
        }
    }
}
